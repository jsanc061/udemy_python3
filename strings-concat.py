first_name = "josh"
last_name = "sanchez"
full_name = first_name + " " + last_name
print(full_name.title())

greeting = "Hello " + full_name.title() + ", How are you?"
print(greeting)

# adding whitespace to strings (tabs and newlines)
# To add a tab use \t

print(first_name+"\t"+last_name)

# To add a newline use \n

print(first_name+"\n"+last_name)

