colors = ["yellow", "blue", "red", "orange", "green"]
print(colors[0:3])
# output - ['yellow', 'blue', 'red']

print(colors[1:4])
# output - ['blue', 'red', 'orange']

print(colors[:4])
# output - ['yellow', 'blue', 'red', 'orange']

print(colors[2:])
# output - ['red', 'orange', 'green']

print(colors[-2:])
# output - ['orange', 'green']



